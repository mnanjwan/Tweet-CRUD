     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $request->validate([
            'tweet' => 'required|max:140'
        ]);

        Tweet::create([

            'body' => $request->tweet
        ]);

        return redirect()->back()->with('success', 'Tweeted 1sec ago');
    }

